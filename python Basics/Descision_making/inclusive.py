"""
check whether a number is in range of 50 and 100 inclusive
yes:num is in range of 50 to 100
no:num is not in range of 100
"""
num=int(input("enter a number:"))#75
if num>=50 and num<=100 :#75>=50 and75<=100
    print(num,"is in range of 50-100")
else:
    print(num,"is not in range of 50-100")



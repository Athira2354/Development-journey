"""
Relational operators are used for comparing two values and returning a boolean result (True or False).
(<, >, <=, >=, ==, !=) are the common relational operators in Python.
"""
num1=100

num2=200


print(num1 < num2)


print(num1 > num2)


print(num1 <= num2)


print(num2 >=num1)


print(num1==num2)


print(num1!=num2)
